// Get popular items for quick search buttons
export function getPopularItems() {
  return Object.entries(ANNIVERSARY_ITEMS)
    .filter(([id, item]) => item.popular)
    .map(([id, item]) => ({ id: parseInt(id), ...item }))
    .slice(0, 8); // Limit to 8 for UI
}

// Search items by name (improved search)
export function searchItemsByName(searchTerm) {
  if (!searchTerm || searchTerm.length < 2) return [];
  
  const term = searchTerm.toLowerCase();
  const results = Object.entries(ANNIVERSARY_ITEMS)
    .filter(([id, item]) => 
      item.name.toLowerCase().includes(term) ||
      item.category.toLowerCase().includes(term)
    )
    .map(([id, item]) => ({ id: parseInt(id), ...item }))
    .sort((a, b) => {
      // Prioritize exact matches and popular items
      const aExact = a.name.toLowerCase().startsWith(term);
      const bExact = b.name.toLowerCase().startsWith(term);
      if (aExact !== bExact) return bExact ? 1 : -1;
      if (a.popular !== b.popular) return b.popular ? 1 : -1;
      return a.name.localeCompare(b.name);
    })
    .slice(0, 15); // Limit to 15 results
    
  return results;
}

